def low_com():							# lowest companion (hay,wood[tree],carrot)

## -- data --##
	hay		=	num_items(Items.Hay)
	wood	=	num_items(Items.Wood)
	carrot	=	num_items(Items.Carrot)
	
## -- calculation -- ##
	lowest_companion = Entities.Grass
	if hay > wood /10 :
		lowest_companion = Entities.Tree	# switch here between bush or tree
	if wood /10 > carrot and hay > carrot :
		lowest_companion = Entities.Carrot
		
## -- output -- ##
	return lowest_companion
